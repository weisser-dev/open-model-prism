export function normalise(id) {
  let s = id.toLowerCase();
  // Strip Bedrock variant suffix  :0  :1
  s = s.replace(/:\d+$/, '');
  // Strip cross-region inference prefix  eu.  us.  ap.  us-gov.  global.
  s = s.replace(/^(?:eu|us|ap|us-gov|global)\./, '');
  // Strip known vendor-dot prefix added by cloud providers
  s = s.replace(/^(?:anthropic|amazon|meta|mistralai|mistral|qwen|cohere|google|microsoft|deepseek|openai|minimax|zai|twelvelabs|nvidia|moonshot|writer)\./, '');
  // Normalise separators → dashes
  s = s.replace(/[\s._/]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');
  // Strip 8-digit date stamps  -20250514  -20240307  etc.
  s = s.replace(/-\d{8}(?=-|$)/, '');
  // Strip 4-digit stamps  -2507  -0307 (after the 8-digit pass)
  s = s.replace(/-\d{4}(?=-|$)/, '');
  // Strip trailing version tags  -v1  -v2  -v1-0  etc.
  s = s.replace(/-v\d+(?:-\d+)?(?=-|$)/, '');
  // Strip -latest
  s = s.replace(/-latest$/, '');
  // Final clean-up
  s = s.replace(/-+/g, '-').replace(/^-|-$/g, '');
  return s;
}

/**
 * Find registry entry for a model ID.
 * 1. Check all pattern arrays for exact normalised match.
 * 2. If nothing found, substring match (longest pattern wins).
 * Returns null if unknown.
 */
export function findModel(modelId) {
  if (!modelId) return null;
  const needle = normalise(modelId);

  // Pass 1: exact match
  for (const entry of MODEL_REGISTRY) {
    for (const p of entry.patterns) {
      if (normalise(p) === needle) return withBenchmarks(entry);
    }
  }

  // Pass 2: needle contains pattern (longest wins → most specific)
  let best = null;
  let bestLen = 0;
  for (const entry of MODEL_REGISTRY) {
    for (const p of entry.patterns) {
      const np = normalise(p);
      if (needle.includes(np) && np.length > bestLen) {
        best = entry;
        bestLen = np.length;
      }
    }
  }
  if (best) return withBenchmarks(best);

  // Pass 3: pattern contains needle (catch short IDs like "o1", "o3")
  for (const entry of MODEL_REGISTRY) {
    for (const p of entry.patterns) {
      const np = normalise(p);
      if (np.includes(needle) && needle.length >= 2) return withBenchmarks(entry);
    }
  }

  return null;
}

/** Attach benchmark scores to a registry entry (non-mutating). */
function withBenchmarks(entry) {
  if (!entry) return null;
  return { ...entry, benchmarks: BENCHMARKS[entry.id] || null };
}

/**
 * Build suggestion object from local registry for a given modelId.
 * Returns null if not found — caller should then try suggestFromWeb().
 */
export function suggestForModel(modelId) {
  const entry = findModel(modelId); // already includes benchmarks via withBenchmarks()
  if (!entry) return null;
  return {
    family:          entry.family,
    vendor:          entry.vendor,
    tier:            entry.tier,
    inputPer1M:      entry.inputPer1M,
    outputPer1M:     entry.outputPer1M,
    contextWindow:   entry.contextWindow,
    maxOutputTokens: entry.maxOutputTokens || null,
    categories:      entry.categories,
    priority:        entry.priority,
    openrouterId:    entry.openrouterId,
    benchmarks:      entry.benchmarks || null,
    source:          'local_registry',
  };
}

/** Return benchmark scores for a list of model IDs (for the preset wizard). */
export function getBenchmarks(modelIds = []) {
  const result = {};
  for (const id of modelIds) {
    const entry = findModel(id);
    if (entry?.benchmarks) result[id] = entry.benchmarks;
  }
  return result;
}

/**
 * Full async suggest: local registry first, then models.dev live data.
 */
export async function suggestForModelAsync(modelId) {
  const local = suggestForModel(modelId);
  if (local) return local;

  try {
    const { suggestFromWeb } = await import('../services/modelEnrichmentService.js');
    return await suggestFromWeb(modelId);
  } catch {
    return null;
  }
}
